                    <h2>How the biggest name in barbeque got its start</h2>
                    <p>David’s Barbeque is one of the last remaining connections to the Bryan Barbecue family. It is currently owned and operated by Jimmy Harris, a fourth-generation barbeque buff.</p>
                    <p>Jimmy’s father, David, and Sonny Bryan of Dallas barbecue fame were first cousins. Elias Bryan, who started a barbecue business in Dallas in 1910, was their grandfather. There are plenty of Sonny Bryan’s Barbecue locations around Dallas, but Sonny sold out to investors in 1989, shortly before his passing. That’s why David’s Barbecue is special. It’s the only joint left with a connection to Elias and Red Bryan that is still run by the family.</p>
                    <p>Red Bryan took over the business in 1929. David Bryan Harris bought one of Red Bryan's stores on North Collins Street in Arlington in 1965. In 1988, after a three-year sabbatical, the family tradition reopened as David's Barbecue on West Park Row in Pantego. If you’re unfamiliar with the town of Pantego, it’s famous for three things: speed traps, bad water, and David’s Barbeque.</p>
